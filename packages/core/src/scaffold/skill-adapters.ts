export function listSupportedAdapters(): string[] {
  return SKILL_ADAPTERS.map((adapter) => adapter.id);
}

export async function syncSkillAdapters(root: string, options?: SyncAdaptersOptions): Promise<SyncAdaptersResult> {
  const force = options?.force ?? false;
  const dryRun = options?.dryRun ?? false;
  const selected = resolveSelectedAdapters(options?.agents);

  const canonicalChanges: AdapterFileChange[] = [];
  for (const file of CANONICAL_FILES) {
    const change = await upsertFile(root, file.path, file.content, {
      force,
      dryRun,
      managed: false,
    });
    canonicalChanges.push(change);
  }

  const adapterChanges: AdapterFileChange[] = [];
  for (const adapter of selected) {
    if (adapter.mode === "pointer") {
      const content = buildPointerContent(adapter.id);
      for (const path of adapter.paths) {
        const change = await upsertFile(root, path, content, {
          force,
          dryRun,
          managed: true,
        });
        adapterChanges.push(change);
      }
      continue;
    }

    for (let i = 0; i < adapter.paths.length; i += 1) {
      const canonical = CANONICAL_FILES[i];
      if (!canonical) {
        adapterChanges.push({ path: adapter.paths[i], action: "skipped", reason: "No canonical source file" });
        continue;
      }

      const change = await upsertFile(root, adapter.paths[i], canonical.content, {
        force,
        dryRun,
        managed: false,
      });
      adapterChanges.push(change);
    }
  }

  return {
    canonical: canonicalChanges,
    adapters: adapterChanges,
    selectedAgents: selected.map((adapter) => adapter.id),
  };
}

function resolveSelectedAdapters(agents?: string[]): SkillAdapterDefinition[] {
  if (!agents || agents.length === 0) {
    return SKILL_ADAPTERS;
  }

  const normalized = new Set(agents.map((agent) => agent.trim().toLowerCase()).filter(Boolean));
  const selected = SKILL_ADAPTERS.filter((adapter) => normalized.has(adapter.id));

  const missing = Array.from(normalized).filter((agent) => !SKILL_ADAPTERS.some((adapter) => adapter.id === agent));

  if (missing.length > 0) {
    throw new Error(`Unsupported adapters: ${missing.join(", ")}`);
  }

  return selected;
}

function buildPointerContent(adapterId: string): string {
  if (adapterId === "cursor") {
    return `---
description: SDD workflow adapter
alwaysApply: false
---

Read .sdd/skill/sdd/SKILL.md for the full SDD workflow.

If needed, also read:
- .sdd/skill/sdd/references/file-format.md
- .sdd/skill/sdd/references/change-requests.md
- .sdd/skill/sdd/references/bugs.md
`;
  }

  return `# SDD Adapter (${adapterId})

Primary instructions: .sdd/skill/sdd/SKILL.md

References:
- .sdd/skill/sdd/references/file-format.md
- .sdd/skill/sdd/references/change-requests.md
- .sdd/skill/sdd/references/bugs.md

Fallback (legacy): .claude/skills/sdd/SKILL.md
`;
}

async function upsertFile(
  root: string,
  relativePath: string,
  content: string,
  options: { force: boolean; dryRun: boolean; managed: boolean },
): Promise<AdapterFileChange> {
  const absPath = resolve(root, relativePath);
  const finalContent = options.managed ? `${MANAGED_HEADER}${content}` : content;

  if (!existsSync(absPath)) {
    if (!options.dryRun) {
      await mkdir(dirname(absPath), { recursive: true });
      await writeFile(absPath, finalContent, "utf-8");
    }
    return { path: relativePath, action: "created" };
  }

  const current = await readFile(absPath, "utf-8");
  if (current === finalContent) {
    return { path: relativePath, action: "skipped", reason: "Already up to date" };
  }

  if (!options.force) {
    return { path: relativePath, action: "skipped", reason: "Exists (use --force to overwrite)" };
  }

  if (!options.dryRun) {
    await writeFile(absPath, finalContent, "utf-8");
  }
  return { path: relativePath, action: "updated" };
}
